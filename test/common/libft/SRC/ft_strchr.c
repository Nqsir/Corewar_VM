#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	stop;
	char	*str;

	str = (char *)s;
	stop = (char)c;
	if (str)
		while (*str)
		{
			if (*str == c)
				return (str);
			str++;
		}
	if (!stop)
		return (str);
	return (NULL);
}
