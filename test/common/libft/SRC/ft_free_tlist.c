#include "libft.h"

void	ft_free_tlist(t_list **list)
{
	if ((void *)*list)
	{
		ft_free_tlist(&((*list)->next));
		ft_memdel((void **)&(*list)->content);
		ft_memdel((void **)list);
	}
	*list = NULL;
}
