#include "libft.h"

char				*ft_strdup(char *src)
{
	int		size_src;
	char	*cpy;

	size_src = ft_strlen(src);
	if (!(cpy = ft_memalloc(sizeof(*src) * size_src + 1)))
		return (0);
	return (ft_strcpy(cpy, src));
}
