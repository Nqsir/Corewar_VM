#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t i;

	if (!dest || !src)
		return (0);
	i = -1;
	while (src[++i] && i < size - 1 && size > 0)
		dest[i] = src[i];
	if (size)
		dest[i] = 0;
	while (src[i])
		i++;
	return (i);
}
