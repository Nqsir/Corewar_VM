#include "libft.h"

t_list			*ft_lstlast(t_list *begin_list)
{
	t_list	*last;

	if (begin_list)
	{
		last = begin_list;
		while (last->next != NULL)
			last = last->next;
		return (last);
	}
	else
		return (NULL);
}
