#include "libft.h"

wchar_t	*ft_wstrcpy(wchar_t *dest, wchar_t *src)
{
	size_t i;

	i = -1;
	while (src[++i])
		dest[i] = src[i];
	dest[i] = 0;
	return (dest);
}
