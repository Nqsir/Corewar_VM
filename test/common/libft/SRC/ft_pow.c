int			ft_pow(int nb, int power)
{
	int		res;

	res = nb;
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	if (power == 1)
		return (nb);
	while (power-- > 1)
		res *= nb;
	return (res);
}
