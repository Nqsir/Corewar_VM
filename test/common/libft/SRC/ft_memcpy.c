#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*ptr;
	size_t				i;

	i = 0;
	ptr = (unsigned char *)src;
	while (i < n)
	{
		ft_memset(dest + i, ptr[i], 1);
		i++;
	}
	return (dest);
}
