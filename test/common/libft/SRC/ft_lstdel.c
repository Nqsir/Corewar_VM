#include "libft.h"
#include <stdlib.h>
#include <unistd.h>

void		ft_lstdel(t_list **list, void (*del)(void *, size_t))
{
	if (!list || !del)
		return ;
	if (!(*list))
		return ;
	ft_lstdel(&((*list)->next), del);
	ft_lstdelone(list, del);
	free(*list);
	*list = NULL;
}
