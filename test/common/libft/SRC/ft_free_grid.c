#include <stdlib.h>

int		ft_free_grid(char ***tab)
{
	int i;

	i = -1;
	while (tab && *tab && (*tab)[++i])
		free((*tab)[i]);
	free(*tab);
	*tab = NULL;
	return (1);
}
