#include "libft.h"

void	*ft_memmove(void *destination, const void *source, size_t n)
{
	unsigned char	*dest;
	unsigned char	*src;
	size_t			i;

	i = 0;
	dest = (unsigned char *)destination;
	src = (unsigned char *)source;
	if ((unsigned long)dest > (unsigned long)src)
	{
		while (n--)
			dest[n] = src[n];
	}
	else
		while (i < n)
		{
			dest[i] = src[i];
			i++;
		}
	return (dest);
}
