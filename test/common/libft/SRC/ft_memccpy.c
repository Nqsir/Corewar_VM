#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char	*ptr;
	unsigned int	i;
	unsigned char	a;

	a = (unsigned char)c;
	i = -1;
	ptr = (unsigned char *)src;
	while (++i < n)
	{
		ft_memset(dest + i, ptr[i], 1);
		if (ptr[i] == a)
			return (dest + i + 1);
	}
	return (NULL);
}
