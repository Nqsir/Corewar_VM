#include "libft.h"

wchar_t	*ft_wstrtoupper(wchar_t *str)
{
	size_t i;

	i = 0;
	if (*str == 0)
		return (str);
	if (str[i] >= 'a' && str[i] <= 'z')
		str[i] -= 32;
	i++;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
