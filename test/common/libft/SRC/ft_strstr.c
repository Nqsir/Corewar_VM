#include "libft.h"

char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		j;
	int		size_to_find;

	size_to_find = 0;
	i = 0;
	while (to_find[size_to_find])
		size_to_find++;
	if (size_to_find == 0)
		return (str);
	while (str[i])
	{
		j = 0;
		while (str[i + j] == to_find[j])
		{
			if (j == size_to_find - 1)
				return (str + i);
			j++;
		}
		i++;
	}
	return (0);
}
